import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    Scanner S=new Scanner(System.in);
	    int t=S.nextInt();
	    for(int i=0;i<t;i++)
	    { 
	        int x=S.nextInt();
	        int y=S.nextInt();
	        int z=(x*y)/100;
	        System.out.println(z);

	}
}
}